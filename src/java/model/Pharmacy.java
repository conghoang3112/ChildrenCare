package model;

/**
 *
 * @author Portgas.D.Ace
 */
public class Pharmacy {
    private int id;
    private String name;

    public Pharmacy() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Pharmacy{" + "id=" + id + ", name=" + name + '}';
    }

    
    
    
}
